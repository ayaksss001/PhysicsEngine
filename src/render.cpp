void drawBody(const Body& body)
{
    const int segments = 30;
    glColor3f(body.r, body.g, body.b);
    glBegin(GL_TRIANGLE_FAN);
    glVertex2f(body.x, body.y);
    for(int i = 0; i<= segments;i++)
    {
        float angle = 2.0f * 3.1415926f * float(i) / segments;
        float dx = body.radius * cosf(angle);
        float dy = body.radius * sinf(angle);
        glVertex2f(body.x + dx, body.y + dy);
    }
    glEnd();
}

